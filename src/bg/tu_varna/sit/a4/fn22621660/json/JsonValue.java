package bg.tu_varna.sit.a4.fn22621660.json;

/**
 * The {@code JsonValue} abstract class serves as a base class for different types of JSON values.
 * It provides a method to convert the value to its JSON string representation.
 */
public abstract class JsonValue
{
    /**
     * Converts the JSON value to its string representation.
     *
     * @return a {@code String} representing the JSON value.
     */
    public abstract String toJsonString();
}
